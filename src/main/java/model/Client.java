package model;

/**
 * Clasa Client reprezinta un client format dintr-un ID unic, nume, varsta si o adresa
 */
public class Client {
    private int id;
    private String name;
    private int age;
    private String address;

    public Client() {
    }

    public Client(int id, String name, int age, String address) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.address = address;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }
}
